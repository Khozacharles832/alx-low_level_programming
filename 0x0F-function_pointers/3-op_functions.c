
#include "3-calc.h"

/**
 * op_add - A function that adds 2 integers
 * op_sub - A function that subtracts 2 
 * op_mul - A function that multiplies 2 integers
 * op_div - A function that divides 2 integers
 * op_mod - A function to return the remainder of 2 ints
 *
 * Return: The result after computation
 */
int op_add(int a, int b)
{
	return (a + b);
}

int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
